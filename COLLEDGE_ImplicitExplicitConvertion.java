public class COLLEDGE_ImplicitExplicitConvertion {

    public static void main(String[] args)
    {
        // Implicit..
        int a = 10;
        double b = a;
        System.out.println(b);

        // Explicit..
        int a1 = 10;
        double b1 = (double)(a1);
        System.out.println(b1);
    }
}
